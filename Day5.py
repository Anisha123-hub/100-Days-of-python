# lets assign value to any two variables and use it to perform arithmetic operations
a = 10
b = 4

# Addition
print(a + b)

# Substraction
print(a -b)

# Multiplication
print(a * b)

# Division
print(a / b)

# Exponential
print(a ** b)

# Modulus
print(a % b)

# Floor Division
print(a // b)